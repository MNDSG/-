/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	uint32_t temp;
	if(__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) == SET)//如果串口2触发空闲中断
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart2);//清除串口2空闲中断标志
		HAL_UART_DMAStop(&huart2);//关闭DAM接收
		temp = huart2.Instance->SR;//清除SR状态寄存器
		temp = huart2.Instance->DR;//读取DR数据寄存器,清除DMA接收中断
		temp = hdma_usart2_rx.Instance->CNDTR;//获取DMA中未传输的数据个数
		Usart2Type.Usart2DMARecLen = USART2_DMA_REC_SIZE - temp;//总计数减去未传输的数据个数，得到已经接收的数据个数
		HAL_UART_AbortCpltCallback(&huart2);
	}
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
	HAL_UART_Receive_DMA(&huart2,Usart2Type.Usart2DMARecBuff,USART2_DMA_REC_SIZE);  //重新打开DMA接收
  /* USER CODE END USART2_IRQn 1 */
}

