void EnableUartIT(void)
{
	__HAL_UART_ENABLE_IT(&huart3, UART_IT_RXNE);//开启串口3接收中断
	__HAL_UART_ENABLE_IT(&huart3, UART_IT_IDLE);//开启串口3空闲接收中断
	__HAL_UART_ENABLE_IT(&huart2, UART_IT_RXNE);//开启串口2接收中断
	__HAL_UART_ENABLE_IT(&huart2, UART_IT_IDLE);//开启串口2空闲接收中断
	__HAL_UART_CLEAR_IDLEFLAG(&huart3);//清除串口3空闲中断标志位
	__HAL_UART_CLEAR_IDLEFLAG(&huart2);//清除串口2空闲中断标志位
	HAL_UART_Receive_DMA(&huart3, Usart3Type.Usart3DMARecBuff, USART3_DMA_REC_SIZE);//使能串口3DMA接收
	HAL_UART_Receive_DMA(&huart2, Usart2Type.Usart2DMARecBuff, USART2_DMA_REC_SIZE);//使能串口2DMA接收
	
}
void HAL_UART_AbortCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART2)
	{
		if(Usart2Type.Usart2RecLen > 0)//证明还有未接收的数据
		{
			memcpy(&Usart2Type.Usart2RecBuff[Usart2Type.Usart2RecLen], Usart2Type.Usart2DMARecBuff, Usart2Type.Usart2DMARecLen);
			Usart2Type.Usart2RecLen += Usart2Type.Usart2DMARecLen;
		}
		else
		{
			memcpy(Usart2Type.Usart2RecBuff, Usart2Type.Usart2DMARecBuff, Usart2Type.Usart2DMARecLen);
			Usart2Type.Usart2RecLen = Usart2Type.Usart2DMARecLen;
		}
		memset(Usart2Type.Usart2DMARecBuff, 0, Usart2Type.Usart2DMARecLen);//清除DMA缓存区
		Usart2Type.Usart2RecFlag = 1;//数据标志位置位
	}
	else if(huart->Instance == USART3)
	{
		if(Usart3Type.Usart3RecLen > 0)
		{
			memcpy(&Usart3Type.Usart3RecBuff[Usart3Type.Usart3RecLen], Usart3Type.Usart3DMARecBuff, Usart3Type.Usart3DMARecLen);
			Usart3Type.Usart3RecLen += Usart3Type.Usart3DMARecLen;
		}
		else
		{
			memcpy(Usart3Type.Usart3RecBuff, Usart3Type.Usart3DMARecBuff, Usart3Type.Usart3DMARecLen);
			Usart3Type.Usart3RecLen = Usart3Type.Usart3DMARecLen;
		}
		memset(Usart3Type.Usart3DMARecBuff, 0, Usart3Type.Usart3DMARecLen);//清除DMA缓存区
		Usart3Type.Usart3RecFlag = 1;//数据标志位置位
	}
}


int fputc(int ch, FILE *f)
{
	while(!(USART1->SR & 0x40));
	USART1->DR = (uint8_t)ch;
	return ch;
}

void MyUsartTransmit(char *Buff, uint16_t len)
{
	uint16_t i;
	for(i = 0;i < len;i++)
	{
		while(!(USART2->SR & 0x40));//有数据发送
		USART2->DR = *Buff++;
	}
	
}
